// ─── Theme inference per mentor situation ───────────────────────────────
// Each kind of mentor message has natural theme affinities.
const KIND_THEMES = {
  realm_briefing:   ["effort", "discipline", "purpose"],
  quest_done:       ["effort", "discipline", "courage"],
  quest_skipped:    ["truth", "humility", "purpose"],
  shadow_encounter: ["truth", "purpose", "wisdom"],
  aspirational:    ["purpose", "wisdom", "learning"],
};

// Realm-id → preferred themes (tunes selection by today's focus area)
const REALM_THEMES = {
  body:        ["effort", "discipline"],
  mind:        ["learning", "attention", "wisdom"],
  heart:       ["service", "purpose"],
  spirit:      ["wisdom", "purpose", "wonder"],
  work:        ["discipline", "purpose", "rigor"],
  // generic / unknown realm just falls through to kind themes
};

// Archetype-aware fallback chain — when archetype's own pool is dry for a
// theme/style, broaden to spiritually-aligned archetypes.
const ARCHETYPE_FALLBACK = {
  stoic:     ["stoic", "monk", "craftsman", "explorer"],
  alchemist: ["alchemist", "craftsman", "monk"],
  explorer:  ["explorer", "stoic", "alchemist"],
  healer:    ["healer", "monk", "stoic"],
  monk:      ["monk", "stoic", "alchemist"],
  craftsman: ["craftsman", "alchemist", "stoic"],
};

// Map a tone id to its preferred quote style.
const TONE_TO_STYLE = {
  simple:        "simple",
  story:         "story",
  philosophical: "philosophical",
};

// ─── Deterministic hash of a string → integer in [0, 2^31) ──────────────
function hashString(s) {
  let h = 5381;
  for (let i = 0; i < s.length; i++) {
    h = ((h << 5) + h + s.charCodeAt(i)) | 0;
  }
  return Math.abs(h);
}

// ─── Score a quote against the requested context ────────────────────────
function scoreQuote(q, { archetypes, themes, style }) {
  let score = 0;
  // Archetype affinity — earlier in the chain = higher score
  for (let i = 0; i < archetypes.length; i++) {
    if (q.archetypes.includes(archetypes[i])) {
      score += 100 - i * 10;
      break;
    }
  }
  // Theme match — earlier in the preferred list = higher score
  for (let i = 0; i < themes.length; i++) {
    if (q.theme === themes[i]) {
      score += 60 - i * 10;
      break;
    }
  }
  // Style match — preferred is best, but other styles still OK
  if (q.style === style) score += 30;
  // Penalty for very long quotes when style is simple (don't overwhelm)
  if (style === "simple" && q.text.split(/\s+/).length > 18) score -= 20;
  return score;
}

// ─── selectQuote — main entry point ─────────────────────────────────────
//
// Returns a quote object {id, author, text, theme, style, archetypes, note}
// or null if pool is empty. Deterministic given ctxKey.
//
// Inputs:
//   archetype : "stoic" | "alchemist" | ...
//   tone      : "simple" | "story" | "philosophical"
//   kind      : "realm_briefing" | "quest_done" | ...
//   realmId   : optional — the realm currently in focus
//   ctxKey    : a string that uniquely identifies the situation; this seeds
//               the deterministic pick so the same situation gives the same quote
//   exclude   : array of quote ids to avoid
//
export function selectQuote({ archetype, tone, kind, realmId, ctxKey = "", exclude = [] }) {
  const archetypes = ARCHETYPE_FALLBACK[archetype] || [archetype];
  const style = TONE_TO_STYLE[tone] || "philosophical";
  const themes = [
    ...(REALM_THEMES[realmId] || []),
    ...(KIND_THEMES[kind] || []),
  ];

  // Score every quote, take top N, then pick deterministically by hash
  const excludeSet = new Set(exclude);
  const scored = [];
  for (const q of QUOTES) {
    if (excludeSet.has(q.id)) continue;
    const s = scoreQuote(q, { archetypes, themes, style });
    if (s > 0) scored.push({ q, s });
  }
  if (scored.length === 0) {
    // Last-resort: any quote whose archetypes overlap the requested one
    const fallback = QUOTES.filter(q =>
      !excludeSet.has(q.id) && q.archetypes.includes(archetype)
    );
    if (fallback.length === 0) {
      // Truly nothing — pick deterministically from ALL quotes
      const idx = hashString(ctxKey) % QUOTES.length;
      return QUOTES[idx];
    }
    const idx = hashString(ctxKey) % fallback.length;
    return fallback[idx];
  }

  // Sort descending by score, take top 25% (or at least 5 candidates).
  scored.sort((a, b) => b.s - a.s);
  const topN = Math.max(5, Math.floor(scored.length * 0.25));
  const top = scored.slice(0, topN).map(x => x.q);
  const idx = hashString(ctxKey) % top.length;
  return top[idx];
}

// ─── Format quote for spoken delivery ───────────────────────────────────
//
// Returns the line that should be appended to the mentor's bridge text
// before being sent to ElevenLabs. Uses natural English so the speech
// flows ("As Marcus Aurelius put it: ...") rather than awkward em-dash.
//
export function formatQuoteForSpeech(quote) {
  if (!quote) return "";
  return `As ${quote.author} put it: "${quote.text}"`;
}
